    private void setResultText(double value) {
        String format = "0.###";
        if (value > LARGE_NUMBER || value < SMALL_NUMBER && value > 0){
            format = "0.###E0";
        }
        if(value < 1 && value > SMALL_NUMBER){
            format = "0.##########";
        }
        DecimalFormat fmt = new DecimalFormat(format);
        resultTextField.setText(fmt.format(value));
    }
    
    private void appendText(String value) {
         String text = resultTextField.getText();
         
         if(text.length() >= RESULT_LENGHT_LIMIT) {
             return;
         }
         
         if (value.equals(".") && text.contains(".")) {
             return;
    }
         
        if (text.startsWith("0")){
            text = value;
        } else {
            text = text + value;
        }
        resultTextField.setText(text);
    }
    
    private double executeOperation(String op, double left, double right){
        switch(op){
            case "+": return left + right;
            case "-": return left - right;
            case "*": return left * right;
            case "/": return left / right;
            case "%": return (left * right) / 100;
            default: return 0;
        }
    }
    
    private void applyOperation(String op) {
        double input = Double.parseDouble(resultTextField.getText());
        if (lastOperation == null) {
            memory = input;
            lastOperation = op;
            setResultText(0);
        } else {
            switch (op) {
                case "%":
                    input = executeOperation(op, memory, input);
                case "=":
                    memory = executeOperation(lastOperation, memory, input);
                    setResultText(memory);
                    memory = 0;
                    lastOperation = null;
                    break;
                default:
                    memory = executeOperation(lastOperation, memory, input);
                    lastOperation = op;
                    setResultText(memory);
                    break;
            }
        }
    }
